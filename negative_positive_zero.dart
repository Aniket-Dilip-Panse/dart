void main() {
  var number = -9.6;
  if (number > 0) {
    print('the number $number is positive');
  } else if (number < 0) {
    print('the number $number is negative');
  } else if (number == 0) {
    print('the number $number is zero');
  } else {
    print("the number is invalid");
  }
}